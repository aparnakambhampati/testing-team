package dayf25;
// Create a method with multiple return values of same data type elements?
public class Task2multipulreturn {
	public static String name(String s1) {
		return s1;
		
	}
	public static String village(String s2) {
		return s2;
	}
	
	public static String city(String s3) {
		return s3;
	}

	public static void main(String[] args) {
		System.out.println(Task2multipulreturn.name("aparna"));
		System.out.println(Task2multipulreturn.village("ckk"));
		System.out.println(Task2multipulreturn.city("hyd"));

	}

}
